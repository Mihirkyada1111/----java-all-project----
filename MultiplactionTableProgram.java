public class MultiplactionTableProgram {
    public static void main(String[] args) {

        int n = 10;
        int range = 10;
        System.out.println("Multiplication Table for Number 9 with Range 1 to 10");
        int i = 1;
        while (i <= range) {

            System.out.println(n + " * " + i + " = " + n * i);

            i++;
        }

    }
}
